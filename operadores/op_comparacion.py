# Los operadores de comparación nos devuelven True o False.

igual_que = 5 == 6  # La comparación es FALSA así que nos devuelve False.

distinto_de = 5 != 6  # la comparación es VERDADERA así que nos devuelve True.

mayor_que = 5 > 6

menor_que = 5 < 6

mayor_o_igual = 5 >= 6

menor_o_igual = 5 <= 6

